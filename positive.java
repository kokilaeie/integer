import java.io.*;
import java.util.*;
class positive
{
public static void main(String []args)
{
Scanner s=new Scanner(System.in);
int n=s.nextInt();
if(n==0)
{
System.out.println("number is zero");
}
else if(n>0)
{
System.out.println("number is positive");
}
else
{
System.out.println("number is negative");
}
}
}
